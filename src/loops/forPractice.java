package loops;
public class forPractice
{
    public static void main(String [] args)
    {
        for(int i = 0; i <= 25; i++)
        {
            System.out.print(i+" ");
        }
        System.out.println("\n");
        
        for (int i = 10; i >= 1; i--)
        {
            System.out.print(i + " ");
        }
        System.out.println("\n");
        
        for(int i = 1; i <= 39; i = i+2)
        {
            System.out.print(i+" ");
        }
        System.out.println("\n");
        
        for(int i = 2; i <= 40; i = i + 2)
        {
            System.out.print(i+" ");
        }
        System.out.println("\n");
        
        for(int i = 0; i <= 200; i = i + 10)
        {
            System.out.print(i+" ");
        }
        System.out.println("\n");
        
        for (int lcv = -10; lcv >= -20; lcv--)
        {
            System.out.print(lcv + " ");
        }
        System.out.println("\n");
        
        for(int i = 0; i <= 10; i++)
        {
            System.out.println("computer");
        }
    }
}